package com.itheima.d3_operator;

/*
      目标：掌握基本算数运算符的使用
      +  -  *  /  %
*/
public class Test1 {
    public static void main(String[] args) {
        System.out.println(10 + 4);
        System.out.println(10 + 4.0);
        System.out.println(10 - 4);
        System.out.println(10 - 4.0);
        System.out.println(10 * 4);
        System.out.println(10 * 4.0);
        System.out.println(10 / 4);
        System.out.println(10 / 4.0);
        System.out.println(10 % 4);
        System.out.println(10 % 4.0);
        System.out.println("--------------------------");
        //字符串拼接
        String name = "钟晨瑶";
        int age = 18;
        char gender = '女';
        System.out.println("姓名： " + name + "\n" + "年龄： " + age + "\n" + "性别： " + gender);


    }
}
