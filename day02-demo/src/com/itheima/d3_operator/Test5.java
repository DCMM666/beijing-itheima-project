package com.itheima.d3_operator;

/*
    目标：掌握关系运算符的使用
    关系运算符是用来比较两个变量或数据的大小关系或者相等关系的

    !=  ==  >  >=  <  <=
 */
public class Test5 {
    public static void main(String[] args) {
        int a = 20;
        int b = 30;
        int c = 40;
        System.out.println(a == b);
        System.out.println(a <= b);
        System.out.println(b >= c);
        System.out.println(b <= c);
        System.out.println(a != c);
        System.out.println(a=c);
        System.out.println(a==c);

    }
}
